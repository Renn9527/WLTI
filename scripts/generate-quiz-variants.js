#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const rootDir = path.resolve(__dirname, '..');
const questionsSource = fs.readFileSync(path.join(rootDir, 'questions.js'), 'utf8');
const context = { console };

context.globalThis = context;
vm.createContext(context);
new vm.Script(`${questionsSource}\nglobalThis.__exports = { questions };`, { filename: 'questions.js' }).runInContext(context);

const { questions } = context.__exports;

const variantBlueprints = [
    {
        id: 'full25',
        label: '原版 25 题',
        shortLabel: '25 题版',
        description: '保留完整题库，结果最细，适合第一次认真测。',
        estimatedMinutes: 5,
        excludeQuestionIds: []
    },
    {
        id: 'lite20',
        label: '轻量 20 题',
        shortLabel: '20 题版',
        description: '删掉 5 道后段题，节奏更紧凑，结果仍然稳定。',
        estimatedMinutes: 4,
        excludeQuestionIds: [20, 21, 22, 24, 25]
    },
    {
        id: 'quick15',
        label: '速测 15 题',
        shortLabel: '15 题版',
        description: '再删 5 道题，适合快速玩一轮，分布会更跳一点。',
        estimatedMinutes: 3,
        excludeQuestionIds: [13, 14, 16, 17, 18, 20, 21, 22, 24, 25]
    }
];

const dimensionOrder = ['W', 'L', 'T', 'I', 'Q'];
const targetFile = path.join(rootDir, 'quiz-variants.js');

main();

function main() {
    const variants = {};

    variantBlueprints.forEach((blueprint, index) => {
        const questionIds = questions
            .filter(question => !blueprint.excludeQuestionIds.includes(question.id))
            .map(question => question.id);
        const dimensionCounts = buildDimensionCounts(questionIds);
        const thresholds = buildThresholds(dimensionCounts);
        const codeWeights = buildCodeWeights(questionIds, thresholds);

        variants[blueprint.id] = {
            id: blueprint.id,
            label: blueprint.label,
            shortLabel: blueprint.shortLabel,
            description: blueprint.description,
            estimatedMinutes: blueprint.estimatedMinutes,
            questionIds,
            questionCount: questionIds.length,
            dimensionCounts,
            thresholds,
            codeWeights
        };
    });

    const lines = [
        '// This file is generated by scripts/generate-quiz-variants.js',
        `const defaultQuizVariantId = ${JSON.stringify(variantBlueprints[0].id)};`,
        `const quizVariants = ${stringifyVariants(variants)};`,
        '',
        'function getQuizVariantConfig(variantId) {',
        '    return quizVariants[variantId] || quizVariants[defaultQuizVariantId];',
        '}',
        ''
    ];

    fs.writeFileSync(targetFile, lines.join('\n'), 'utf8');
    console.log(`Generated ${path.basename(targetFile)}`);
}

function buildDimensionCounts(questionIds) {
    const idSet = new Set(questionIds);
    const counts = { W: 0, L: 0, T: 0, I: 0, Q: 0 };

    questions.forEach(question => {
        if (!idSet.has(question.id)) {
            return;
        }

        question.dimension.forEach(code => {
            counts[code] += 1;
        });
    });

    return counts;
}

function buildThresholds(dimensionCounts) {
    const thresholds = {};

    Object.entries(dimensionCounts).forEach(([code, questionCount]) => {
        let states = new Map([[0, 1]]);

        for (let step = 0; step < questionCount; step += 1) {
            const next = new Map();

            states.forEach((count, sum) => {
                [1, 2, 3].forEach(score => {
                    const nextSum = sum + score;
                    next.set(nextSum, (next.get(nextSum) || 0) + count);
                });
            });

            states = next;
        }

        const totalOutcomes = 3 ** questionCount;
        let cumulativeProbability = 0;
        let low = questionCount;
        let mid = questionCount * 2;

        [...states.entries()]
            .sort((left, right) => left[0] - right[0])
            .forEach(([sum, count]) => {
                cumulativeProbability += count / totalOutcomes;

                if (low === questionCount && cumulativeProbability >= 1 / 3) {
                    low = sum;
                }

                if (mid === questionCount * 2 && cumulativeProbability >= 2 / 3) {
                    mid = sum;
                }
            });

        thresholds[code] = { low, mid };
    });

    return thresholds;
}

function buildCodeWeights(questionIds, thresholds) {
    const idSet = new Set(questionIds);
    let states = new Map([[JSON.stringify({ W: 0, L: 0, T: 0, I: 0, Q: 0 }), 1]]);

    questions.forEach(question => {
        if (!idSet.has(question.id)) {
            return;
        }

        const next = new Map();

        states.forEach((count, key) => {
            const baseScores = JSON.parse(key);

            question.options.forEach(option => {
                const nextScores = { ...baseScores };

                Object.entries(option.scores).forEach(([dimension, value]) => {
                    nextScores[dimension] += value;
                });

                const nextKey = JSON.stringify(nextScores);
                next.set(nextKey, (next.get(nextKey) || 0) + count);
            });
        });

        states = next;
    });

    const totalOutcomes = 3 ** questionIds.length;
    const weights = {};

    states.forEach((count, key) => {
        const scores = JSON.parse(key);
        const typeCode = dimensionOrder.map(code => {
            const { low, mid } = thresholds[code];

            if (scores[code] <= low) {
                return 'L';
            }

            if (scores[code] <= mid) {
                return 'M';
            }

            return 'H';
        }).join('');

        weights[typeCode] = Number(((weights[typeCode] || 0) + count / totalOutcomes).toFixed(12));
    });

    return Object.fromEntries(Object.entries(weights).sort((left, right) => left[0].localeCompare(right[0], 'en')));
}

function stringifyVariants(variants) {
    return JSON.stringify(variants, null, 4);
}
